#include "ft_printf.h"

void	ft_putstr_n(char *s, int *n)
{
	int	i;

	i = 0;
	if (s == (void *) '\0')
	{
		write(1, "(null)", 6);
		*n += 6;
		return ;
	}
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
	*n += i;
}
