#include "ft_printf.h"

void	ft_putint(int i, int *n)
{
	if (i == -2147483648)
	{
		write(1, "-2147483648", 11);
		*n += 11;
		return ;
	}
	if (i < 0)
	{
		ft_putchar('-');
		*n += 1;
		i *= -1;
	}
	if (i < 10)
	{
		ft_putchar(i + '0');
		*n += 1;
	}
	else
	{
		ft_putint(i / 10, n);
		ft_putint(i % 10, n);
	}
}
